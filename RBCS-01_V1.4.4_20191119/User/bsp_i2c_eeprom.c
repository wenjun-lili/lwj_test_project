/**
  * @file   bsp_eeprom_24xx.c
	* @brief  串行EEPROM 24xx驱动模块
	* @datail 实现24xx系列EEPROM的读写操作。写操作采用页写模式提高写入效率。
	* @autor  先亚生物
	* @version V1.0
	* @date   2016-02-01
  */
/**
	应用说明：访问串行EEPROM前，请先调用一次 bsp_InitI2C()函数配置好I2C相关的GPIO.
  */
#include "bsp_i2c_eeprom.h"
#include "bsp_i2c_gpio.h"
#include "stdio.h"
#include "utility.h"

/**
  * @brief  I2CEEPROM_Init(void)
	* @datail eeprom初始化
	* @param  none
	* @return none
	*/
void I2CEEPROM_Init(void)
{
    if (ee_CheckOk() == 0)
    {
//        printf("error");
        while (1);	/* 等待 */
    }
}
/**
  * @brief  ee_CheckOk(void)
	* @datail 判断串行EERPOM是否正常
	* @param  none
  * @return 0：不正常
            1：正常
	*/

uint8_t ee_CheckOk(void)
{
    if (i2c_CheckDevice(EE_DEV_ADDR) == 0)
    {
        return 1;
    }
    else
    {
        /* 失败后，切记发送I2C总线停止信号 */
        i2c_Stop();
        return 0;
    }
}
/**
  * @brief  ee_ReadBytes(uint8_t *_pReadBuf, uint16_t _usAddress, uint16_t _usSize)
	* @datail 从串行EEPROM指定地址处开始读取若干数据
	* @param  _usAddress : 起始地址
  		      _usSize : 数据长度，单位为字节
       		  _pReadBuf :存放读到的数据缓冲区指针
  * @return 0：失败
            1：成功
	*/

uint8_t ee_ReadBytes(uint8_t *_pReadBuf, uint16_t _usAddress, uint16_t _usSize)
{
    uint16_t i;

    /*  采用串行EEPROM随即读取指令序列，连续读取若干字节 */

    /* 第1步：发起I2C总线启动信号*/
    i2c_Start();

    /* 第2步：发起控制字节，高7bit是地址，bit0是读取地址，0表示写，1表示读 */
#if EE_ADDR_A8 == 1
    i2c_SendByte(EE_DEV_ADDR | I2C_WR | ((_usAddress >> 7) & 0x0E));	/* 此处是写指令 */
#else
    i2c_SendByte(EE_DEV_ADDR | I2C_WR);	/* 此处是写指令 */
#endif

    /* 第3 发送ACK  */
    if (i2c_WaitAck() != 0)
    {
        goto cmd_fail;	/* EEPROM器件无响应 */
    }

    /*  第4步：发送字节地址，24C02只有256字节，因此1个字节就够了，如果24C04以上，那么此处需要连发多个地址 */
    if (EE_ADDR_BYTES == 1)
    {
        i2c_SendByte((uint8_t)_usAddress);
        if (i2c_WaitAck() != 0)
        {
            goto cmd_fail;	/* EEPROM????? */
        }
    }
    else
    {
        i2c_SendByte(_usAddress >> 8);
        if (i2c_WaitAck() != 0)
        {
            goto cmd_fail;	/* EEPROM器件无应答 */
        }

        i2c_SendByte(_usAddress);
        if (i2c_WaitAck() != 0)
        {
            goto cmd_fail;	/* EEPROM器件无应答 */
        }
    }

    /* 第6步：重新启动I2C总线。下面开始读取数据 */
    i2c_Start();

    /*  第7步：发起控制字节，高7bit是地址 ,bit0是读写控制位，0表示写，1表示读*/
#if EE_ADDR_A8 == 1
    i2c_SendByte(EE_DEV_ADDR | I2C_RD | ((_usAddress >> 7) & 0x0E));	/* 此处是写指令 */
#else
    i2c_SendByte(EE_DEV_ADDR | I2C_RD);	/* 此处是写指令 */
#endif

    /* 第8步：发送ACk */
    if (i2c_WaitAck() != 0)
    {
        goto cmd_fail;	/* EEPROM无器件相应 */
    }

    /* 第9步：循环读取数据 */
    for (i = 0; i < _usSize; i++)
    {
        _pReadBuf[i] = i2c_ReadByte();	/* 读1个字节 */

        /* 每读完1个字节后，需要发送ACK，最后一个字节不需要Ack,发NAck*/
        if (i != _usSize - 1)
        {
            i2c_Ack();	/* 中间字节读完后，CPU产生ACK信号(驱动SDA=0)*/
        }
        else
        {
            i2c_NAck();	/* 最后1个字节读完后，cpu 产生NACK信号（驱动SDA=1）) */
        }
    }
    /* 发送I2C总线停止信号 */
    i2c_Stop();
    return 1;	/* 执行成功 */

cmd_fail: /* 命令执行失败后，切记发送停止信号 ，避免影响I2C总线上其他设备*/
    /* 发送I2C总线停止信号  */
    i2c_Stop();
    return 0;
}
/**
  * @brief  ee_WriteBytes(uint8_t *_pWriteBuf, uint16_t _usAddress, uint16_t _usSize)
	* @datail 向串行EEPROM指定地址写入若干数据，采用页写操作写入效率
	* @param  _usAddress : 起始地址
  		      _usSize : 数据长度，单位为字节
       		  pWriteBuf : 存放读到的数据的缓冲区指针
  * @return 0：失败
            1：成功
	*/

uint8_t ee_WriteBytes(uint8_t *_pWriteBuf, uint16_t _usAddress, uint16_t _usSize)
{
    uint16_t i,m;
    uint16_t usAddr;

    /*
    	 写串行EEPROM不像读操作可以连续读取很多字节，每次写操作只能在同一个page
       对于24XX02/page size=8
       简单的处理方法为：按字节写操作模式，每次写1个字节，都发送地址
       为了提高连续写的效率：本函数采用page Write操作
    */

    usAddr = _usAddress;
    for (i = 0; i < _usSize; i++)
    {
        /*  当发送第一个字节或者是页面首地址时，需要重新发起启动信号和地址*/
        if ((i == 0) || (usAddr & (EE_PAGE_SIZE - 1)) == 0)
        {
            /* 第0步：发停止信号，启动内部操作 */
            i2c_Stop();

            /*  通过检查器件应答的方式，判断内部写操作是否完成，一般小于10ms CLK

              频率为200KHz时，查询首次次数为30次
            */
            for (m = 0; m < 1000; m++)
            {
                /* 第1步：发起I2C总线启动信号 */
                i2c_Start();

                /*  第2步：发送控制字节，高7bit是地址，bit0是读写控制位，0表示写，1表示读*/

#if EE_ADDR_A8 == 1
                i2c_SendByte(EE_DEV_ADDR | I2C_WR | ((usAddr >> 7) & 0x0E));	/* ?????? */
#else
                i2c_SendByte(EE_DEV_ADDR | I2C_WR);
#endif

                /*  发送一个时钟，判断器件是否正确应答 */
                if (i2c_WaitAck() == 0)
                {
                    break;
                }
            }
            if (m  == 1000)
            {
                goto cmd_fail;	/* EEPROM器件写超时 */
            }

            /*  第4步：发送字节地址，24C02只有256字节，因此1个字节就够了，如果24C04以上，那么此处需要连发多个地址 */
            if (EE_ADDR_BYTES == 1)
            {
                i2c_SendByte((uint8_t)usAddr);
                if (i2c_WaitAck() != 0)
                {
                    goto cmd_fail;	/* EEPROM器件无应答*/
                }
            }
            else
            {
                i2c_SendByte(usAddr >> 8);
                if (i2c_WaitAck() != 0)
                {
                    goto cmd_fail;	/* EEPROM器件无应答 */
                }

                i2c_SendByte(usAddr);
                if (i2c_WaitAck() != 0)
                {
                    goto cmd_fail;	/* EEPROM器件无应答 */
                }
            }
        }

        /* 第6步：开始写入数据 */
        i2c_SendByte(_pWriteBuf[i]);

        /* 发送ACK */
        if (i2c_WaitAck() != 0)
        {
            goto cmd_fail;	/* EEPROM器件无应答 */
        }


        usAddr++;	/* 地址增1 */
    }

    /*  命令执行成功 ，发送I2C总线停止信号*/
    i2c_Stop();

    /*  通过检查器件应答的方式，判断内部写操作是否完成，一般小于10ms

    CLK频率为200KHz时，查询次数为30次左右
    */
    for (m = 0; m < 1000; m++)
    {
        /* 第1步：发起I2C总线启动信号*/
        i2c_Start();

        /* 第2步：发起控制字节，高7bit是地址，bit0是读写控制位，0表示写，1表示读 */
#if EE_ADDR_A8 == 1
        i2c_SendByte(EE_DEV_ADDR | I2C_WR | ((_usAddress >> 7) & 0x0E));	/* ?????? */
#else
        i2c_SendByte(EE_DEV_ADDR | I2C_WR);	/* 此处是写指令 */
#endif

        /*  第3步 发送一个时钟，判断器件是否正确应答 */
        if (i2c_WaitAck() == 0)
        {
            break;
        }
    }
    if (m  == 1000)
    {
        goto cmd_fail;	/* EEPROM器件写超时 */
    }

    return 1;

cmd_fail: /*  命令执行失败后，切记发送停止信号，避免影响I2C总线上其他设备 */
    /*  发送I2C总线停止信号 */
    i2c_Stop();

    return 0;
}
